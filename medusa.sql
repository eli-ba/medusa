create or replace view v_byrch as
select byrch.breakstone_id, byrch.farman_of_faircastle_id, byrch.yronwood_of_yronwood_id
from byrch
left join breakstone on byrch.breakstone_id = breakstone.id
left join farman_of_faircastle on byrch.farman_of_faircastle_id = farman_of_faircastle.id
left join yronwood_of_yronwood on byrch.yronwood_of_yronwood_id = yronwood_of_yronwood.id;

